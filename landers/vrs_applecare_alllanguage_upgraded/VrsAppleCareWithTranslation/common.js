var now_seconds = 90,
  timer = document.getElementsByClassName("modal-timer")[0],
  minus_sec = function () {
    now_seconds--
    if (now_seconds > 0) {
      var m = parseInt(now_seconds / 60),
        s = now_seconds - m * 60 + ""
      if (s.length <= 1) s = "0" + s
      timer.innerHTML = m + ":" + s
      setTimeout(minus_sec, 1000)
    } else {
      timer.innerHTML = "0:00"
    }
  }
